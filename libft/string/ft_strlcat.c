#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	s;

	i = ft_strlen(dst);
	s = (size <= i ? size : i) + (size_t)ft_strlen(src);
	if ((int)size - (int)i > 0)
		ft_strncat(dst, src, size - i - 1);
	return (s);
}
