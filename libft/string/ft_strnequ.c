#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if (!s1 || !s2)
		return (0);
	i = n;
	while (n && *s1 && *s2)
	{
		n--;
		if (*s1++ != *s2++)
			return (0);
	}
	return ((n <= i && *s1 == *s2) || n == 0 ? 1 : 0);
}
