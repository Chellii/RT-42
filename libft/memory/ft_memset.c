#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	unsigned char	*sc;

	sc = (unsigned char *)s;
	while (len)
		sc[--len] = (unsigned char)c;
	return ((s = (void *)sc));
}
