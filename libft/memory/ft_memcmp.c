#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char a;
	unsigned char b;

	while (n--)
	{
		b = *(unsigned char *)s2++;
		a = *(unsigned char *)s1++;
		if (a - b)
			return (a - b);
	}
	return (!(a - b) ? 0 : a - b);
}
