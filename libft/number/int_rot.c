#include "libft.h"

bool	int_rot(int *t, size_t size)
{
	int			i;
	const int	n = t[size - 1];

	i = size;
	while (--i > 0)
		t[i] = t[i - 1];
	t[0] = n;
	return (size < 2 ? 0 : 1);
}
