long long	ft_pow(long long n, int p)
{
	long long ret;
	long long m;

	ret = 1;
	m = n;
	while (p)
	{
		ret *= p & 1 ? m : 1;
		m *= m;
		p >>= 1;
	}
	return (ret);
}
