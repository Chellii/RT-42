#include "libft.h"

bool	ft_isdouble(const char *s)
{
	bool	dot;

	dot = 0;
	if (s && (*s == '-' || *s == '+'))
		s++;
	while (s && *s && (ft_isdigit(*s) || *s == '.'))
	{
		if (*s == '.' && dot)
			return (0);
		if (*s == '.' && !dot)
			dot = 1;
		s++;
		if (!*s)
			return (1);
	}
	return (0);
}
