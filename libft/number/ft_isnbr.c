#include "libft.h"

bool	ft_isnbr(const char *s)
{
	if (s && (*s == '-' || *s == '+'))
		s++;
	while (s && *s && ft_isdigit(*s))
	{
		s++;
		if (!*s)
			return (1);
	}
	return (0);
}
