#include "libft.h"

bool	int_rrot(int *t, size_t size)
{
	const int	n = t[0];
	int			i;

	if (size > 1)
	{
		i = 0;
		while ((size_t)++i <= size - 1)
			t[i - 1] = t[i];
		t[size - 1] = n;
	}
	return (size < 2 ? 0 : 1);
}
